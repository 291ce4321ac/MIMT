% %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
function modemenu_CBF(objh,~)
	thismode = get(objh,'value');
	whichpanel = find(strcmp(msrgb,methodstrings{thismode}));
	for m = 1:length(panels)
		if m ~= whichpanel
			set(panels(m),'visible','off')
		else
			set(panels(m),'visible','on')
		end
	end

	if strcmp(methodstrings{thismode},'levels')
		updatehistogram();
		updatelvsliders(1);
	end
	
	updatedisplay(inpict,'reset');
	incrementpreview();
end


% %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
function setparam(objh,~,param,idx)
	switch param			
		case 'meanlinesmode'
			meanlinesmode = get(objh,'value');
			
		case 'mlorientation'
			mlorientation = get(objh,'value');
			
		case 'selectedtweakmode'
			selectedtweakmode = get(objh,'value');
			resetparams();
			setchannellabels();
			
		case 'chval'
			val = str2double(get(objh,'string'));
			if cvecinv(idx) == 1
				cvec(idx) = -val;
			else 
				cvec(idx) = val;
			end
			if val > cvecmax(idx)
				cvecmax(idx) = val;
			end
			updatesliders(idx)
			
		case 'chvalslider'
			val = get(objh,'value');
			if ishue(idx)
				% uuugh dammit don't tell me i actually wrote that
				% well, that's another thing on the list of sleeper garbage code to fix
				set(handles.chbox(idx),'string',num2str(val));
			else
				set(handles.chbox(idx),'string',num2str(abs(val)));
			end
			if cvecinv(idx) == 1
				cvec(idx) = -val;
			else 
				cvec(idx) = val;
			end	
			updatesliders(idx)
			
		case 'channelmax'
			cvecmax(idx) = abs(str2double(get(objh,'string')));
			updatesliders(idx)
			
		case 'invertchannel'
			cvecinv(idx) = get(handles.chinvertcheckbox(idx),'value');
			cvec(idx) = -cvec(idx);
			
		case 'resetallchans'
			cvec = [1 1 1];
			cvecinv = [0 0 0];
			cmax = 2;
			cvecmax = cvec*cmax;
			initsliders(1);
			initsliders(2);
			initsliders(3);
			
		case 'levelval'
			val = str2double(get(objh,'string'));
			if idx == 5
				lvvec(idx) = max(val,0);
				if val > gammamax
						gammamax = val;
				end
			elseif idx == 6
				lvvec(idx) = max(val,0);
				if val > contrastmax
						contrastmax = val;
				end
			else
				lvvec(idx) = max(min(val,1),0);
			end
			updatelvsliders(idx)
			
		case 'levelslider'
			val = get(objh,'value');
			set(handles.lvbox(idx),'string',num2str(val));
			lvvec(idx) = val;
			updatelvsliders(idx)
			
		case 'gammamax'
			gammamax = abs(str2double(get(objh,'string')));
			updatelvsliders(5)
			
		case 'contrastmax'
			contrastmax = abs(str2double(get(objh,'string')));
			updatelvsliders(6)
			
