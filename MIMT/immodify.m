function resetparams()
	thistweakmode = tweakmodestrings{selectedtweakmode};
	switch thistweakmode
		case {'hsl','hsv','hsi','hsy','hsyp','huslab','huslpab','husluv','huslok','huslpuv','huslpok'}
			transition(2) = 1;
		case {'lchab','lchuv','lchsr','lchok','ych'}
			transition(2) = 2;
		case 'rgb'
			transition(2) = 3;
	end
	switch lasttweakmode
		case {'hsl','hsv','hsi','hsy','hsyp','huslab','huslpab','husluv','huslok','huslpuv','huslpok'}
			transition(1) = 1;
		case {'lchab','lchuv','lchsr','lchok','ych'}
			transition(1) = 2;
		case 'rgb'
			transition(1) = 3;
	end
	
	if all(transition == [1 2]) || all(transition == [2 1])
		cvec = fliplr(cvec);
		cvecmax = fliplr(cvecmax);
		cvecinv = fliplr(cvecinv);
	elseif any(transition == 3) && any(transition ~= 3)
		cvec = [1 1 1];
		cvecmax = cvec*cmax;
		cvecinv = [0 0 0];
	end
	lasttweakmode = thistweakmode;
end

